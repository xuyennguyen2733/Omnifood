/**
 * SMOOTH SCROLLING ANIMATION
 */

// selects ALL anchors that have 'link' in class name
const allLinks = document.querySelectorAll("a:link");
allLinks.forEach(function (link) {
  link.addEventListener("click", function (e) {
    e.preventDefault();
    const href = link.getAttribute("href");

    // scrolls back to tops
    if (href === "#")
      window.scrollTo({
        top: 0,
        behavior: "smooth",
      });

    // scrolls to other link
    if (href !== "#" && href.startsWith("#")) {
      const section = document.querySelector(href);
      section.scrollIntoView({ behavior: "smooth" });
    }

    // closes mobile navigation
    if (link.classList.contains("main-nav-link"))
      header.classList.toggle("nav-open");
  });
});

/**
 * STICKY NAVIGATION
 */

const sectionHero = document.querySelector(".section-hero");
const observer = new IntersectionObserver(
  function (entries) {
    const ent = entries[0];
    if (!ent.isIntersecting) {
      document.body.classList.add("sticky");
    }
    if (ent.isIntersecting) {
      document.body.classList.remove("sticky");
    }
  },
  {
    root: null, // observed in the viewport
    thredshold: 0, // when section-hero is completely out of viewport
    rootMargin: "-80px",
  }
);
observer.observe(sectionHero);

